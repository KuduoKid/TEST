players = ['charles', 'martina', 'michael', 'florence', 'eli']
print(players[0:3])
"""
代码打印该列表的一个切片，其中只包含前三名队员
可以生成列表的任意子集。例如，如果要提取列表的第二、第三和第四个元素
如果没有指定第一个索引,Python将自动从列表开头开始
"""
print(players[3:])# 提取从第4个元素开始，到列表末尾的所有元素
print(players[-2:])# 负数索引返回离列表末尾相应距离的元素,输出名单上的最后两名队员











